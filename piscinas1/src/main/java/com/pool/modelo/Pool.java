package com.pool.modelo;

/**
 *
 * @author Sergio
 */
public class Pool {
    
    private int estado;
    private int orden;
    
    public Pool(){
    }
    
    public Pool(int estado, int orden){
        this.estado=estado;
        this.orden=orden;
        
    }

    /**
     * @return the estado
     */
    public int getEstado() {
        return estado;
    }

    /**
     * @param estado the estado to set
     */
    public void setEstado(int estado) {
        this.estado = estado;
    }

    /**
     * @return the orden
     */
    public int getOrden() {
        return orden;
    }

    /**
     * @param orden the orden to set
     */
    public void setOrden(int orden) {
        this.orden = orden;
    }
}
